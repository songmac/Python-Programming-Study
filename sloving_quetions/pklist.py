import math as m

def list_max(list1, list2, list3):
    new_list = list1 + list2 + list3
    print(max(new_list))

def list_min(list1, list2, list3):
    new_list = list1 + list2 + list3
    print(min(new_list))


def list_avg(list1, list2, list3):
    new_list = list1 + list2 + list3
    print(sum(new_list)/len(new_list))